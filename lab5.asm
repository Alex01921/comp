;ODD OR EVEN NUMBERS:
DATA SEGMENT
 N DW 12H
 MSG1 DB "ODD$"
 MSG2 DB "EVEN$"
DATA ENDS
PRINT MACRO MSG
 MOV AH, 09H
 LEA DX, MSG
 INT 21H
ENDM
CODE SEGMENT
 ASSUME CS:CODE, DS:DATA, EX:EXTRA
START:
 MOV AX, DATA
 MOV DS, AX
 MOV AX, N
 TEST AX, 1
 JZ EVEN
 PRINT MSG1
 JMP LAST
EVEN:
 PRINT MSG2
LAST:
 MOV AH, 4CH
 INT 21H
 CODE ENDS
END START 

;POSITIVE AND NEGATIVE NUMBERS:
DATA SEGMENT
 N DW 12H
 MSG1 DB "POSITIVE$"
 MSG2 DB "NEGATIVE$"
 DATA ENDS
PRINT MACRO MSG
 MOV AH,09H
 LEA DX,MSG
 INT 21H
 MOV AH,4CH
 INT 21H
ENDM
CODE SEGMENT
 ASSUME CS:CODE,DS:DATA,ES:EXTRA
 START:MOV AX,DATA
 MOV DS,AX
 MOV AX,N
 ROL AX,1
 JNC NEG
 POS:PRINT MSG2
 NEG:PRINT MSG1
 LAST:MOV AH,4DH
 INT 21H
 CODE ENDS
END START
