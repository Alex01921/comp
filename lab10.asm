DATA SEGMENT
 STR1 DB "HELLO$"
 STR2 DB "WORLD$"
 DATA ENDS
CODE SEGMENT
 ASSUME DS:DATA,CS:CODE
 START:
 MOV AX,DATA
 MOV DS,AX
 MOV SI,OFFSET STR1
 NEXT:
 MOV AL,[SI]
 CMP AL,"$"
 JE EXIT ;IF AL=="$", THEN IT WILL JUMP TO EXIT
 INC SI
 JMP NEXT
 EXIT:MOV DI,OFFSET STR2
 UP:MOV AL,[DI]
 CMP AL,"$"
 JE EXIT1
 MOV [SI],AL
 INC SI
 INC DI
 JMP UP
 EXIT1:
 MOV AL,"$"
 MOV [SI],AL
 MOV AH,4CH
 INT 21H
 CODE ENDS
END START
